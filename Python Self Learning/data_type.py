v3 = float(20.5) // Float
v4 = complex(1j) // Complex
v5 = list(("apple", "banana", "cherry")) // List
v6 = tuple(("apple", "banana", "cherry")) // Tuple
v7 = range(5) // Range
v8 = dict(name="John", age=36) // Dictionary
v9 = set(("apple", "banana", "cherry")) // Set
v10 = frozenset(("apple", "banana", "cherry")) // Frozenset
